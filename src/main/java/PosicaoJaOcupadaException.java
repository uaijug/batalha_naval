
public class PosicaoJaOcupadaException extends Exception {

	public PosicaoJaOcupadaException() {

	}

	public PosicaoJaOcupadaException(String message) {
		super(message);
	}

	public PosicaoJaOcupadaException(Throwable cause) {
		super(cause);
	}

	public PosicaoJaOcupadaException(String message, Throwable cause) {
		super(message, cause);
	}

	public PosicaoJaOcupadaException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
