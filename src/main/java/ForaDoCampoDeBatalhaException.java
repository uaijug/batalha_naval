
public class ForaDoCampoDeBatalhaException extends Exception {

	public ForaDoCampoDeBatalhaException() {
		super();
	}

	public ForaDoCampoDeBatalhaException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ForaDoCampoDeBatalhaException(String message, Throwable cause) {
		super(message, cause);
	}

	public ForaDoCampoDeBatalhaException(String message) {
		super(message);
	}

	public ForaDoCampoDeBatalhaException(Throwable cause) {
		super(cause);
	}

}
